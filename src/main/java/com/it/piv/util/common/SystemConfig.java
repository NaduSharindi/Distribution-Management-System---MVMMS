package com.it.piv.util.common;

public class SystemConfig {

	private String windowsReportDir;
	private String windowsReportOutDir;
	private String linuxReportDir;
	private String linuxReportOutDir;
	
	public String getWindowsReportDir() {
		return windowsReportDir;
	}
	public void setWindowsReportDir(String windowsReportDir) {
		this.windowsReportDir = windowsReportDir;
	}
	public String getWindowsReportOutDir() {
		return windowsReportOutDir;
	}
	public void setWindowsReportOutDir(String windowsReportOutDir) {
		this.windowsReportOutDir = windowsReportOutDir;
	}
	public String getLinuxReportDir() {
		return linuxReportDir;
	}
	public void setLinuxReportDir(String linuxReportDir) {
		this.linuxReportDir = linuxReportDir;
	}
	public String getLinuxReportOutDir() {
		return linuxReportOutDir;
	}
	public void setLinuxReportOutDir(String linuxReportOutDir) {
		this.linuxReportOutDir = linuxReportOutDir;
	}
	
	
	
	
}
